/**
 * add - add operations
 * @a: first number
 * @b: second number
 *
 * Description: adds two numbers
 *
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction operations
 * @a: first number
 * @b: second number
 *
 * Description: subtracts b from a
 *
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication operations
 * @a: first number
 * @b: second number
 *
 * Description: multiples a and a
 *
 * Return: int
 */
 int mul(int a, int b)
 {
 	return (a * b);
 }
 
 /**
 * mod - modulus operations
 * @a: first number
 * @b: second number
 *
 * Description: returns the modulus of a and b
 *
 * Return: int
 */
  int mod(int a, int b)
 {
 	return (a % b);
 }
 
